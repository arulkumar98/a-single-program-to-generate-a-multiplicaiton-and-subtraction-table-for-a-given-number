class table
{
	public static void main(String arg[])
	{
		// Given number as n
		int n = 77;

		for (int i = 1; i <= 10; i++) {
			System.out.println(n + " x " + i + " = " + (n * i));
		}

		for (int j = 0; j <= 10; j++) {
			System.out.println(n + " - " + j + " = " + (n - j));
        }
        
	}
}


